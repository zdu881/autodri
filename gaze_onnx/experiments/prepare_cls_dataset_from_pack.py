#!/usr/bin/env python3
# -*- coding: utf-8 -*-

"""Build YOLOv8-cls dataset from a multi-domain annotation pack.

Expected input directory:
  <samples-dir>/
    images/
    labels.csv  (generated by web_label_tool.py)

This script does not re-crop faces. It reuses ROI-cropped images from the
annotation pack and creates:
  <out-dir>/
    train/<ClassName>/*.jpg
    val/<ClassName>/*.jpg
    split_manifest.csv

Usage examples:
  # Domain-holdout evaluation (test cross-car/person generalization)
  python gaze_onnx/experiments/prepare_cls_dataset_from_pack.py \
    --samples-dir gaze_onnx/experiments/anno_two_domain_v3_ratio_roi_run1 \
    --out-dir gaze_onnx/experiments/cls_dataset_two_domain_holdout_car2 \
    --split-mode domain_holdout \
    --val-domain car2_person2 \
    --augment-minority \
    --target-train-per-class 550

  # Final training split (both domains in train/val, stratified by domain+class)
  python gaze_onnx/experiments/prepare_cls_dataset_from_pack.py \
    --samples-dir gaze_onnx/experiments/anno_two_domain_v3_ratio_roi_run1 \
    --out-dir gaze_onnx/experiments/cls_dataset_two_domain_stratified \
    --split-mode domain_stratified \
    --val-ratio 0.2 \
    --augment-minority
"""

import argparse
import csv
import random
from collections import Counter, defaultdict
from dataclasses import dataclass
from pathlib import Path
from typing import Dict, Iterable, List, Tuple

import cv2
import numpy as np


VALID_LABELS = ("Forward", "Non-Forward", "In-Car", "Other")


@dataclass(frozen=True)
class Item:
    src_path: Path
    rel_img: str
    label: str
    domain: str
    frame_id: int
    timestamp: float
    video: str


def parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser(description="Prepare YOLOv8-cls dataset from ROI annotation pack")
    p.add_argument("--samples-dir", required=True, help="Annotation pack dir containing images/ and labels.csv")
    p.add_argument("--out-dir", required=True, help="Output YOLOv8-cls dataset dir")
    p.add_argument(
        "--split-mode",
        choices=("domain_holdout", "domain_stratified", "random"),
        default="domain_holdout",
        help="How to split train/val",
    )
    p.add_argument("--val-domain", default="", help="Validation domain when split-mode=domain_holdout")
    p.add_argument("--val-ratio", type=float, default=0.2, help="Validation ratio for non-holdout modes")
    p.add_argument("--seed", type=int, default=42)
    p.add_argument(
        "--labels",
        nargs="+",
        default=list(VALID_LABELS),
        help="Labels to include (default: Forward Non-Forward In-Car Other)",
    )
    p.add_argument("--augment-minority", action="store_true", help="Augment minority classes in train split")
    p.add_argument(
        "--target-train-per-class",
        type=int,
        default=0,
        help="Augmented target count per train class (0 uses max class count)",
    )
    p.add_argument(
        "--target-train-map",
        type=str,
        default="",
        help=(
            "Optional explicit target map, e.g. "
            "'Forward=320,In-Car=260,Non-Forward=220,Other=60'. "
            "If set, it overrides --target-train-per-class for listed labels."
        ),
    )
    p.add_argument(
        "--max-augment-ratio",
        type=float,
        default=8.0,
        help="Upper bound on synthetic expansion per class: target <= raw_count * ratio",
    )
    p.add_argument(
        "--copy-mode",
        choices=("copy", "hardlink", "symlink"),
        default="copy",
        help="How to materialize images into output dataset",
    )
    return p.parse_args()


def _safe_int(x: str) -> int:
    try:
        return int(float((x or "").strip()))
    except Exception:
        return -1


def _safe_float(x: str) -> float:
    try:
        return float((x or "").strip())
    except Exception:
        return 0.0


def read_items(samples_dir: Path, include_labels: Iterable[str]) -> List[Item]:
    include = {x.strip() for x in include_labels}
    labels_path = samples_dir / "labels.csv"
    if not labels_path.exists():
        raise FileNotFoundError(f"labels.csv not found: {labels_path}")

    items: List[Item] = []
    with labels_path.open("r", encoding="utf-8", newline="") as f:
        for row in csv.DictReader(f):
            label = (row.get("label") or row.get("Human_Label") or "").strip()
            if label in ("", "Unknown", "Skip"):
                continue
            if label not in include:
                continue

            rel_img = str(row.get("img", "")).strip()
            if not rel_img:
                continue
            src_path = samples_dir / rel_img
            if not src_path.exists():
                continue

            items.append(
                Item(
                    src_path=src_path,
                    rel_img=rel_img,
                    label=label,
                    domain=str(row.get("Domain", "")).strip(),
                    frame_id=_safe_int(row.get("FrameID", "")),
                    timestamp=_safe_float(row.get("Timestamp", "")),
                    video=str(row.get("Video", "")).strip(),
                )
            )
    return items


def split_items(items: List[Item], split_mode: str, val_ratio: float, seed: int, val_domain: str) -> Tuple[List[Item], List[Item]]:
    if not items:
        return [], []

    rng = random.Random(seed)
    val_ratio = min(0.95, max(0.01, float(val_ratio)))

    if split_mode == "domain_holdout":
        domains = sorted({x.domain for x in items if x.domain})
        if not domains:
            raise ValueError("No Domain field found in labels.csv; cannot use domain_holdout mode.")
        picked = val_domain.strip() or domains[-1]
        if picked not in domains:
            raise ValueError(f"Requested --val-domain={picked} not in domains={domains}")
        train_items = [x for x in items if x.domain != picked]
        val_items = [x for x in items if x.domain == picked]
        if not train_items or not val_items:
            raise ValueError("domain_holdout split produced empty train or val set.")
        return train_items, val_items

    if split_mode == "domain_stratified":
        train_items: List[Item] = []
        val_items: List[Item] = []
        buckets: Dict[Tuple[str, str], List[Item]] = defaultdict(list)
        for x in items:
            buckets[(x.domain, x.label)].append(x)

        for _, rows in buckets.items():
            rng.shuffle(rows)
            n_val = max(1, int(round(len(rows) * val_ratio)))
            n_val = min(len(rows) - 1, n_val) if len(rows) > 1 else 1
            val_items.extend(rows[:n_val])
            train_items.extend(rows[n_val:])
        return train_items, val_items

    # random stratified by class only
    train_items = []
    val_items = []
    by_class: Dict[str, List[Item]] = defaultdict(list)
    for x in items:
        by_class[x.label].append(x)
    for _, rows in by_class.items():
        rng.shuffle(rows)
        n_val = max(1, int(round(len(rows) * val_ratio)))
        n_val = min(len(rows) - 1, n_val) if len(rows) > 1 else 1
        val_items.extend(rows[:n_val])
        train_items.extend(rows[n_val:])
    return train_items, val_items


def materialize(src: Path, dst: Path, mode: str) -> None:
    dst.parent.mkdir(parents=True, exist_ok=True)
    if dst.exists():
        return
    if mode == "copy":
        data = src.read_bytes()
        dst.write_bytes(data)
    elif mode == "hardlink":
        dst.hardlink_to(src)
    elif mode == "symlink":
        dst.symlink_to(src.resolve())
    else:
        raise ValueError(f"unknown copy mode: {mode}")


def parse_target_map(s: str) -> Dict[str, int]:
    out: Dict[str, int] = {}
    text = (s or "").strip()
    if not text:
        return out
    for chunk in text.split(","):
        c = chunk.strip()
        if not c:
            continue
        if "=" not in c:
            raise ValueError(f"Bad --target-train-map item '{c}', expected Label=Number")
        k, v = c.split("=", 1)
        label = k.strip()
        if label not in VALID_LABELS:
            raise ValueError(f"Unknown label '{label}' in --target-train-map")
        n = int(float(v.strip()))
        if n < 1:
            raise ValueError(f"Target for '{label}' must be >=1")
        out[label] = n
    return out


def aug_photometric(img: np.ndarray, rng: random.Random) -> np.ndarray:
    """Strong but realistic augmentation for illumination/domain robustness."""
    out = img.copy()

    # Brightness + contrast
    alpha = rng.uniform(0.65, 1.45)  # contrast
    beta = rng.uniform(-40.0, 40.0)  # brightness shift
    out = np.clip(out.astype(np.float32) * alpha + beta, 0, 255).astype(np.uint8)

    # Gamma
    gamma = rng.uniform(0.6, 1.6)
    lut = (np.linspace(0, 1, 256) ** (1.0 / gamma) * 255.0).astype(np.uint8)
    out = cv2.LUT(out, lut)

    # Optional grayscale to reduce dependence on color cues
    if rng.random() < 0.35:
        gray = cv2.cvtColor(out, cv2.COLOR_BGR2GRAY)
        out = cv2.cvtColor(gray, cv2.COLOR_GRAY2BGR)

    # Noise
    if rng.random() < 0.5:
        sigma = rng.uniform(2.0, 10.0)
        noise = np.random.normal(0.0, sigma, out.shape).astype(np.float32)
        out = np.clip(out.astype(np.float32) + noise, 0, 255).astype(np.uint8)

    # Motion/defocus blur
    if rng.random() < 0.35:
        k = rng.choice([3, 5])
        out = cv2.GaussianBlur(out, (k, k), 0)

    # Mild jpeg artifact simulation
    if rng.random() < 0.35:
        quality = int(rng.uniform(35, 85))
        ok, enc = cv2.imencode(".jpg", out, [int(cv2.IMWRITE_JPEG_QUALITY), quality])
        if ok:
            dec = cv2.imdecode(enc, cv2.IMREAD_COLOR)
            if dec is not None:
                out = dec
    return out


def augment_minority_train(
    out_dir: Path,
    all_rows: List[Dict[str, str]],
    target_by_class: Dict[str, int],
    seed: int,
) -> None:
    rng = random.Random(seed)
    train_base = [r for r in all_rows if r["split"] == "train" and r["augmented"] == "0"]
    by_cls: Dict[str, List[Dict[str, str]]] = defaultdict(list)
    for r in train_base:
        by_cls[r["label"]].append(r)

    for cls, rows in sorted(by_cls.items()):
        if cls not in target_by_class:
            continue
        target = int(target_by_class[cls])
        cur = sum(1 for r in all_rows if r["split"] == "train" and r["label"] == cls)
        if cur >= target:
            continue
        need = target - cur
        cls_dir = out_dir / "train" / cls
        aug_count = 0
        for i in range(need):
            src = rng.choice(rows)
            src_path = out_dir / src["dst_rel"]
            img = cv2.imread(str(src_path))
            if img is None:
                continue
            aug = aug_photometric(img, rng)
            aug_name = f"aug_{i:06d}_{Path(src['dst_rel']).name}"
            dst_path = cls_dir / aug_name
            cv2.imwrite(str(dst_path), aug)
            all_rows.append(
                {
                    "split": "train",
                    "label": cls,
                    "domain": src["domain"],
                    "frame_id": src["frame_id"],
                    "timestamp": src["timestamp"],
                    "video": src["video"],
                    "src_rel": src["src_rel"],
                    "dst_rel": str(dst_path.relative_to(out_dir)),
                    "augmented": "1",
                }
            )
            aug_count += 1
        print(f"[AUG] {cls}: {cur} -> {cur + aug_count}")


def main() -> None:
    args = parse_args()
    samples_dir = Path(args.samples_dir)
    out_dir = Path(args.out_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    include_labels = [x for x in args.labels if x in VALID_LABELS]
    if not include_labels:
        raise SystemExit(f"No valid labels in --labels. Valid labels: {VALID_LABELS}")

    items = read_items(samples_dir, include_labels=include_labels)
    if not items:
        raise SystemExit("No labeled samples found. Check labels.csv.")

    train_items, val_items = split_items(
        items=items,
        split_mode=args.split_mode,
        val_ratio=args.val_ratio,
        seed=args.seed,
        val_domain=args.val_domain,
    )

    print(f"Loaded labeled samples: {len(items)}")
    print(f"Split mode: {args.split_mode}")
    if args.split_mode == "domain_holdout":
        picked = args.val_domain.strip() or sorted({x.domain for x in items if x.domain})[-1]
        print(f"Validation domain: {picked}")
    print(f"Train/Val: {len(train_items)} / {len(val_items)}")

    rows_out: List[Dict[str, str]] = []

    def save_split(split_name: str, rows: List[Item]) -> None:
        for x in rows:
            base = Path(x.rel_img).name
            safe_domain = x.domain if x.domain else "unknown_domain"
            dst_name = f"{safe_domain}__{base}"
            dst_rel = Path(split_name) / x.label / dst_name
            dst_path = out_dir / dst_rel
            materialize(x.src_path, dst_path, args.copy_mode)
            rows_out.append(
                {
                    "split": split_name,
                    "label": x.label,
                    "domain": x.domain,
                    "frame_id": str(x.frame_id),
                    "timestamp": f"{x.timestamp:.3f}",
                    "video": x.video,
                    "src_rel": x.rel_img,
                    "dst_rel": str(dst_rel),
                    "augmented": "0",
                }
            )

    save_split("train", train_items)
    save_split("val", val_items)

    train_non_aug = [r for r in rows_out if r["split"] == "train" and r["augmented"] == "0"]
    if args.augment_minority:
        cls_counts = Counter(r["label"] for r in train_non_aug)
        default_target = args.target_train_per_class if args.target_train_per_class > 0 else max(cls_counts.values())
        target_map = parse_target_map(args.target_train_map)
        target_by_class: Dict[str, int] = {}
        ratio = max(1.0, float(args.max_augment_ratio))
        for cls, raw in sorted(cls_counts.items()):
            requested = int(target_map.get(cls, default_target))
            max_allowed = max(raw, int(round(raw * ratio)))
            target = min(requested, max_allowed)
            target_by_class[cls] = target
        print(f"Augment targets by class: {target_by_class}  (max_augment_ratio={ratio:.2f})")
        augment_minority_train(out_dir=out_dir, all_rows=rows_out, target_by_class=target_by_class, seed=args.seed)

    # Persist split manifest.
    split_manifest = out_dir / "split_manifest.csv"
    fields = ["split", "label", "domain", "frame_id", "timestamp", "video", "src_rel", "dst_rel", "augmented"]
    with split_manifest.open("w", encoding="utf-8", newline="") as f:
        w = csv.DictWriter(f, fieldnames=fields)
        w.writeheader()
        w.writerows(rows_out)

    # Print summary.
    print("\n=== Dataset summary ===")
    for split in ("train", "val"):
        split_rows = [r for r in rows_out if r["split"] == split]
        c = Counter(r["label"] for r in split_rows)
        d = Counter(r["domain"] for r in split_rows)
        print(f"{split}: n={len(split_rows)}  labels={dict(c)}  domains={dict(d)}")

    print(f"\nOutput dataset: {out_dir}")
    print(f"Split manifest: {split_manifest}")


if __name__ == "__main__":
    main()
