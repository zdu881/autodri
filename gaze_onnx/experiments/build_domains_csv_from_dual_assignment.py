#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""Build domains CSV (for annotation pack) from dual-ROI assignment CSV.

Input:
- CSV generated by assign_dual_roi.py with columns:
  video,status,gaze_roi,wheel_roi,swapped,assignment_uncertain,...

Output:
- CSV compatible with create_multidomain_annotation_pack.py:
  domain_id,video,roi_x1,roi_y1,roi_x2,roi_y2,n_samples
"""

import argparse
import csv
from collections import Counter
from pathlib import Path
from typing import Dict, List, Optional, Tuple


ROI = Tuple[int, int, int, int]


def parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser(description="Build domains CSV from dual-ROI assignment CSV")
    p.add_argument("--assignment-csv", required=True, help="CSV from assign_dual_roi.py")
    p.add_argument("--out-csv", required=True, help="Output domains CSV")
    p.add_argument("--task", choices=["gaze", "wheel"], default="gaze", help="Use gaze_roi or wheel_roi")
    p.add_argument("--domain-id", default="p1", help="domain_id written to output rows")
    p.add_argument("--samples-per-video", type=int, default=12, help="n_samples for normal rows")
    p.add_argument(
        "--samples-per-video-uncertain",
        type=int,
        default=16,
        help="n_samples for assignment_uncertain=1 rows",
    )
    p.add_argument("--include-uncertain", action="store_true", help="Include assignment_uncertain=1 rows")
    p.add_argument(
        "--require-status-ok",
        action="store_true",
        help="Only include rows where status=ok",
    )
    p.add_argument(
        "--video-contains",
        default="",
        help="Optional substring filter on video path",
    )
    p.add_argument(
        "--participant-majority-roi",
        action="store_true",
        help="Force all output rows to use the dominant ROI among filtered rows.",
    )
    return p.parse_args()


def parse_roi(s: str) -> Optional[ROI]:
    t = str(s).strip().replace(" ", "")
    if not t:
        return None
    parts = t.split(",")
    if len(parts) != 4:
        return None
    try:
        x1, y1, x2, y2 = [int(float(v)) for v in parts]
    except Exception:
        return None
    if x2 <= x1 or y2 <= y1:
        return None
    return (x1, y1, x2, y2)


def build_rows(args: argparse.Namespace) -> Tuple[List[Dict[str, str]], Dict[str, int]]:
    src = Path(args.assignment_csv)
    if not src.exists():
        raise FileNotFoundError(src)

    roi_col = "gaze_roi" if args.task == "gaze" else "wheel_roi"
    out_rows: List[Dict[str, str]] = []
    stats = {
        "in_total": 0,
        "skip_status": 0,
        "skip_uncertain": 0,
        "skip_video_filter": 0,
        "skip_bad_roi": 0,
        "out_total": 0,
        "majority_override_count": 0,
    }

    with src.open("r", encoding="utf-8", newline="") as f:
        r = csv.DictReader(f)
        fields = set(r.fieldnames or [])
        required = {"video", "status", "assignment_uncertain", roi_col}
        miss = required - fields
        if miss:
            raise ValueError(f"Missing columns in {src}: {sorted(miss)}")

        for row in r:
            stats["in_total"] += 1
            video = str(row.get("video", "")).strip()
            status = str(row.get("status", "")).strip().lower()
            uncertain = str(row.get("assignment_uncertain", "0")).strip() == "1"

            if args.require_status_ok and status != "ok":
                stats["skip_status"] += 1
                continue
            if (not args.include_uncertain) and uncertain:
                stats["skip_uncertain"] += 1
                continue
            if args.video_contains and args.video_contains not in video:
                stats["skip_video_filter"] += 1
                continue

            roi = parse_roi(row.get(roi_col, ""))
            if roi is None:
                stats["skip_bad_roi"] += 1
                continue

            n_samples = int(args.samples_per_video_uncertain if uncertain else args.samples_per_video)
            x1, y1, x2, y2 = roi
            out_rows.append(
                {
                    "domain_id": str(args.domain_id),
                    "video": video,
                    "roi_x1": str(x1),
                    "roi_y1": str(y1),
                    "roi_x2": str(x2),
                    "roi_y2": str(y2),
                    "n_samples": str(max(1, n_samples)),
                    "source_swapped": str(row.get("swapped", "")),
                    "source_uncertain": "1" if uncertain else "0",
                }
            )

    if args.participant_majority_roi and out_rows:
        roi_counter = Counter(
            (row["roi_x1"], row["roi_y1"], row["roi_x2"], row["roi_y2"]) for row in out_rows
        )
        majority_roi, _ = roi_counter.most_common(1)[0]
        override_count = 0
        for row in out_rows:
            cur_roi = (row["roi_x1"], row["roi_y1"], row["roi_x2"], row["roi_y2"])
            if cur_roi != majority_roi:
                override_count += 1
                row["roi_x1"], row["roi_y1"], row["roi_x2"], row["roi_y2"] = majority_roi
        stats["majority_override_count"] = override_count

    stats["out_total"] = len(out_rows)
    return out_rows, stats


def main() -> None:
    args = parse_args()
    rows, stats = build_rows(args)

    out = Path(args.out_csv)
    out.parent.mkdir(parents=True, exist_ok=True)
    fields = [
        "domain_id",
        "video",
        "roi_x1",
        "roi_y1",
        "roi_x2",
        "roi_y2",
        "n_samples",
        "source_swapped",
        "source_uncertain",
    ]
    with out.open("w", encoding="utf-8", newline="") as f:
        w = csv.DictWriter(f, fieldnames=fields)
        w.writeheader()
        for row in rows:
            w.writerow(row)

    print(f"Saved: {out}")
    print(
        "Stats: "
        + ", ".join(
            [
                f"in_total={stats['in_total']}",
                f"out_total={stats['out_total']}",
                f"skip_status={stats['skip_status']}",
                f"skip_uncertain={stats['skip_uncertain']}",
                f"skip_video_filter={stats['skip_video_filter']}",
                f"skip_bad_roi={stats['skip_bad_roi']}",
                f"majority_override_count={stats['majority_override_count']}",
            ]
        )
    )


if __name__ == "__main__":
    main()
